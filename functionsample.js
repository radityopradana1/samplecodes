// =================== list of function ===================

function sumTotalPrice (priceOne, priceTwo) {

    var totalPrice = priceOne + priceTwo

    return totalPrice
}

function substractTotalPrice (priceOne, priceTwo) {

    var totalPrice = priceOne - priceTwo

    return totalPrice
}

function multiplyTotalPrice (priceOne, priceTwo) {

    var totalPrice = priceOne * priceTwo

    return totalPrice
}

function divideTotalPrice (priceOne, priceTwo) {

    var totalPrice = priceOne / priceTwo

    return totalPrice
}




